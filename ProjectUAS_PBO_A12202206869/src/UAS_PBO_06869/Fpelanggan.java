    private void txtKdpelangganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtKdpelangganActionPerformed
        String kdpelanggan = txtKdpelanggan.getText().trim(); 
        
        if (kdpelanggan.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Kode Barang tidak boleh kosong");
            txtKdpelanggan.requestFocus();
            return;
        }

        String sql = "SELECT COUNT(*) AS count FROM pelanggan WHERE kdpelanggan = ?";
        try (Connection conn = Koneksi.configDB();
            PreparedStatement pstmt = conn.prepareStatement(sql)) {

            pstmt.setString(1, kdpelanggan);
            try (java.sql.ResultSet res = pstmt.executeQuery()) {
                System.out.println("Query executed: " + sql);
                if (res.next()) {
                    int count = res.getInt("count");
                    System.out.println("Count retrieved: " + count);
                    if (count > 0){
                        // Jika kode barang sudah ada, tampilkan pesan peringatan
                        JOptionPane.showMessageDialog(null, "Kode Pelanggan " + kdpelanggan + " sudah ada dalam database.");
                    } else {
                        // Jika kode barang belum ada, lakukan tindakan yang sesuai
                        JOptionPane.showMessageDialog(null, "Kode Pelanggan " + kdpelanggan + " belum ada dalam database, Anda dapat melanjutkan.");
                        databaru = true;
                    } 
                } else {
                    // Handling case if no result is returned
                    JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat mendapatkan hasil dari database.");
                }                    
            }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat mengakses database: " + e.getMessage());
                e.printStackTrace();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace();
        }              // TODO add your handling code here:
    }//GEN-LAST:event_txtKdpelangganActionPerformed

