    private void cmdCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCariActionPerformed
        aktif(true);
        tombol = "Cari";
        txtKdpelanggan.requestFocus();
        String kode = txtKdpelanggan.getText().trim();

        // Periksa apakah kode pelanggan tidak kosong
        if (kode.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Kode Pelanggan tidak boleh kosong");
            txtKdpelanggan.requestFocus();
            return;
        }

        String sql = "SELECT * FROM pelanggan WHERE kdpelanggan='"+txtKdpelanggan.getText()+"'";
        try (java.sql.Connection conn = (java.sql.Connection) Koneksi.configDB();
        java.sql.Statement stm=conn.createStatement()) {
            
        try (java.sql.ResultSet res = stm.executeQuery(sql)) {
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Kode Pelanggan");               
            model.addColumn("Nama Pelanggan");
            model.addColumn("Alamat");
            model.addColumn("Telepon");

            boolean found = false;
            while (res.next()) {
                found = true;
                model.addRow(new Object[]{                
                    res.getString("kdpelanggan"),
                    res.getString("nama_pelanggan"),
                    res.getString("alamat"),
                    res.getString("telepon"),
                    });
                }

                tabeldata.setModel(model);

                if (found) {
                    JOptionPane.showMessageDialog(null, "Data " + txtKdpelanggan.getText() + " berhasil ditemukan");
                } else {
                    JOptionPane.showMessageDialog(null, "Data " + txtKdpelanggan.getText() + " tidak ditemukan");
                }
            
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat mengakses database: " + e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_cmdCariActionPerformed

